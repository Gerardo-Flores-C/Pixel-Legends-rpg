package Enums;

public enum WeaponType {
    ESPADA_DEL_AMANECER,        // Una espada brillante que emite un resplandor dorado. Aumenta el daño contra enemigos oscuros y tiene una probabilidad de cegar temporalmente a los oponentes.
    ARCO_DEL_SUSURRO,           // Un arco ligero hecho de madera mágica que permite disparar flechas silenciosas. Sus flechas causan daño por envenenamiento y pueden activar trampas a distancia.
    Báculo_DE_LA_TEMPORADA,     // Un báculo decorado con gemas que representa los cuatro elementos. Permite lanzar hechizos elementales.
    LANZA_DE_HIELO,             // Forjada en el corazón de un glaciar, esta lanza puede congelar a los enemigos al impacto.
    ESPADA_MALDITA              // Una espada negra con runas que drena la salud del portador, pero es poderosa contra el Cursed Knight.
}
